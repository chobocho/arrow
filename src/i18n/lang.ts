let currentLang: LangCode = 'ko';

export function setLang(code: LangCode): void {
  currentLang = code;
}

export function getLang(): LangCode {
  return currentLang;
}

export function t(key: string): string {
  return strings[currentLang][key] ?? strings.en[key] ?? key;
}
